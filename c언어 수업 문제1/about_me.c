// 경력을 4바이트 2진수로 출력하는 함수
void printBinary(int num) {
    for (int i = 31; i >= 0; i--) {
        printf("%d", (num >> i) & 1);
        if (i % 8 == 0 && i != 0) {
            printf(" "); // 8비트마다 공백 추가
        }
    }
}

